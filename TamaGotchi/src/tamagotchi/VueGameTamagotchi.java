    private void actualiserStatus() {
        monTama.faucheuse();
        santeJProgressBar4.setValue(monTama.getSante() * 10);
        poidsJProgressBar2.setValue(monTama.getPoids() * 10);
        hygieneJProgressBar.setValue(monTama.getHygiene() * 10);
        faimJProgressBar3.setValue(monTama.getFaim() * 10);
        besoinJProgressBar1.setValue(monTama.getBesoin() * 10);
        humeurJProgressBar5.setValue(monTama.getHumeur() * 10);
        if (monTama.isLife() == false) {
            VueGifjLabel.setIcon(imageAdd("kreeper_Dead", "png"));
            backButtonjPanel.setVisible(false);
            testStartjButton1.setVisible(true);
        }

        OutDialjTextArea.setText(monTama.checkStatus());
    }

